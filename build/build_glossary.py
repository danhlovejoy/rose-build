#!/usr/bin/env python3
"""
build_glossary.py — Glossary HTML Generator

Reads glossary.json from the project root and generates a glossary HTML
source file for each course directory. These files use <link> to reference
course-styles.css, just like any other source file, and are processed by
the normal CSS inliner during the build.

Usage:
    python3 build_glossary.py <project_root>

Generates:
    <project_root>/aiml2003/glossary.html
    <project_root>/aiml2013/glossary.html

Deterministic. No LLM. No external dependencies. Python 3.8+ stdlib only.
"""

import json
import os
import sys
from html import escape


COURSES = {
    "nlp": {
        "dir": "aiml2003",
        "title": "AIML 2003: Introduction to Natural Language Processing",
        "short": "NLP",
        "filter": "nlp",
    },
    "cv": {
        "dir": "aiml2013",
        "title": "AIML 2013: Introduction to Computer Vision",
        "short": "CV",
        "filter": "cv",
    },
}


def load_glossary(root):
    path = os.path.join(root, "glossary.json")
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    return data["terms"]


def filter_terms(terms, course_key):
    """Return terms that include this course, sorted alphabetically."""
    filtered = [t for t in terms if course_key in t["courses"]]
    filtered.sort(key=lambda t: t["term"].lower())
    return filtered


def course_tags(courses):
    """Return HTML tag spans for course membership."""
    if set(courses) == {"nlp", "cv"}:
        return '<span class="tag tag-required">Both</span>'
    elif "nlp" in courses:
        return '<span class="tag tag-read">NLP</span>'
    else:
        return '<span class="tag tag-video">CV</span>'


def term_slug(term_str):
    """Convert a term to a URL-safe slug for anchor links."""
    import re
    return re.sub(r'[^a-z0-9]+', '-', term_str.lower()).strip('-')


def first_letter(term):
    """Return uppercase first letter for section grouping."""
    ch = term["term"][0].upper()
    # Group digits under #
    if ch.isdigit():
        return "#"
    return ch


def generate_html(terms, course_info):
    """Generate a complete HTML source file for one course glossary."""
    lines = []
    lines.append('<!DOCTYPE html>')
    lines.append('<html lang="en">')
    lines.append('<head>')
    lines.append('<meta charset="UTF-8">')
    lines.append('<meta name="viewport" content="width=device-width, initial-scale=1.0">')
    lines.append(f'<title>Glossary | {escape(course_info["title"])}</title>')
    lines.append('<link rel="stylesheet" href="../course-styles.css">')
    lines.append('</head>')
    lines.append('<body>')
    lines.append('')
    lines.append('<!-- Generated from glossary.json - do not edit this file directly. -->')
    lines.append('<!-- To add or update terms, edit glossary.json and rebuild. -->')
    lines.append('')
    lines.append(f'<h1>Glossary</h1>')
    lines.append(f'<p class="subtitle">{escape(course_info["title"])}</p>')
    lines.append('')
    lines.append('<div class="time-box">')
    lines.append(f'  <strong>{len(terms)} terms</strong> from Modules 1&ndash;'
                 f'{max((t["module_introduced"] for t in terms), default=1)}. '
                 f'This page is rebuilt automatically as new modules are added. '
                 f'Each definition links to a longer explanation.')
    lines.append('</div>')
    lines.append('')

    # Letter nav
    letters = sorted(set(first_letter(t) for t in terms))
    nav_parts = []
    for letter in letters:
        nav_parts.append(f'<a href="#letter-{letter}" style="margin-right:0.5rem; '
                         f'font-weight:600;">{letter}</a>')
    lines.append(f'<p style="margin:1.5rem 0;">{"".join(nav_parts)}</p>')
    lines.append('')

    # Terms grouped by letter
    current_letter = None
    for t in terms:
        letter = first_letter(t)
        if letter != current_letter:
            if current_letter is not None:
                lines.append('')
            current_letter = letter
            lines.append(f'<h2 id="letter-{letter}">{letter}</h2>')
            lines.append('')

        term_escaped = escape(t["term"])
        defn_escaped = escape(t["definition"])
        link_text = escape(t["link_text"])
        link_url = escape(t["link_url"])
        tags = course_tags(t["courses"])
        slug = term_slug(t["term"])

        lines.append(f'<div class="section-card" id="{slug}" style="margin-bottom:0.75rem;">')
        lines.append(f'  <p style="margin:0 0 0.25rem 0;"><strong>{term_escaped}</strong> {tags}</p>')
        lines.append(f'  <p style="margin:0 0 0.25rem 0;">{defn_escaped}</p>')
        lines.append(f'  <p style="margin:0;"><a href="{link_url}" target="_blank">'
                     f'{link_text} &rarr;</a></p>')
        lines.append('</div>')

    lines.append('')
    lines.append('</body>')
    lines.append('</html>')

    return "\n".join(lines)


def main():
    if len(sys.argv) < 2:
        print("Usage: python3 build_glossary.py <project_root>")
        sys.exit(1)

    root = sys.argv[1]
    terms = load_glossary(root)

    for key, info in COURSES.items():
        filtered = filter_terms(terms, info["filter"])
        html = generate_html(filtered, info)
        out_dir = os.path.join(root, info["dir"])
        os.makedirs(out_dir, exist_ok=True)
        out_path = os.path.join(out_dir, "glossary.html")
        with open(out_path, "w", encoding="utf-8") as f:
            f.write(html)
        print(f"  {info['dir']}/glossary.html ({len(filtered)} terms)")


if __name__ == "__main__":
    main()
